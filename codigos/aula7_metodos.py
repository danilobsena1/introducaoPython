def soma(a, b):
    return a + b


def subtracao(a, b):
    return a - b


def multiplicacao(a, b):
    return a * b


def divisao(a, b):
    return a / b


print("Soma: {}".format(soma(1, 2)))
print("Subtração: {}".format(subtracao(3, 4)))
print("Multiplicação: {}".format(multiplicacao(5, 6)))
print("Divisão: {}".format(divisao(19, 2)))
